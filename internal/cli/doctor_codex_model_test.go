func TestCheckCodexModelOwnershipText_UserOwnedAndExplicitPoliciesPass(t *testing.T) {
	tests := []struct {
		name    string
		content string
		policy  string
	}{
		{
			name: "user-owned marker",
			content: `# Codex configuration (auto-generated by Autopus-ADK)
# Autopus: user-owned Codex model settings: model, model_reasoning_effort
model = "custom/model"
model_reasoning_effort = "high"
`,
		},
		{
			name: "explicit inherit custom tuple",
			content: `# Codex configuration (auto-generated by Autopus-ADK)
model = "custom/model"
model_reasoning_effort = "high"
`,
			policy: config.SupervisorModelPolicyInherit,
		},
		{
			name:    "explicit inherit",
			content: "# Codex configuration (auto-generated by Autopus-ADK)\napproval_policy = \"on-request\"\n",
			policy:  config.SupervisorModelPolicyInherit,
		},
		{name: "explicit quality", content: legacyCodexProjectConfig, policy: config.SupervisorModelPolicyQuality},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			dir, cfg := writeLegacyCodexDoctorFixture(t, tt.content, true)
			cfg.Quality.SupervisorModelPolicy = tt.policy
			var out bytes.Buffer

			ok := checkCodexModelOwnershipText(&out, dir, cfg)

			assert.True(t, ok)
			assert.NotContains(t, out.String(), "auto quality supervisor")
			assert.NotContains(t, out.String(), "auto update")
		})
	}
}

