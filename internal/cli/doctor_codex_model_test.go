func TestCollectCodexModelOwnershipCheck_MigratableLegacyWarns(t *testing.T) {
	dir, cfg := writeLegacyCodexDoctorFixture(t, legacyCodexProjectConfig, true)
	report := doctorJSONReport{status: jsonStatusOK}

	report.collectCodexModelOwnershipCheck(dir, cfg)

	assert.Equal(t, jsonStatusWarn, report.status)
	require.Len(t, report.checks, 1)
	assert.Equal(t, "doctor.codex.supervisor_model_policy", report.checks[0].ID)
	assert.Equal(t, "warn", report.checks[0].Status)
	assert.Contains(t, report.checks[0].Detail, "auto update")
	require.Len(t, report.warnings, 1)
	assert.Equal(t, "legacy_codex_model_shadowing", report.warnings[0].Code)
}

func TestCodexModelOwnershipCheck_DoesNotModifyProjectFiles(t *testing.T) {
	dir, cfg := writeLegacyCodexDoctorFixture(t, legacyCodexProjectConfig, true)
	configPath := filepath.Join(dir, ".codex", "config.toml")
	harnessPath := filepath.Join(dir, "autopus.yaml")
	require.NoError(t, config.Save(dir, cfg))
	configBefore, err := os.ReadFile(configPath)
	require.NoError(t, err)
	harnessBefore, err := os.ReadFile(harnessPath)
	require.NoError(t, err)

	var out bytes.Buffer
	assert.False(t, checkCodexModelOwnershipText(&out, dir, cfg))
	report := doctorJSONReport{status: jsonStatusOK}
	report.collectCodexModelOwnershipCheck(dir, cfg)

	configAfter, err := os.ReadFile(configPath)
	require.NoError(t, err)
	harnessAfter, err := os.ReadFile(harnessPath)
	require.NoError(t, err)
	assert.Equal(t, configBefore, configAfter)
	assert.Equal(t, harnessBefore, harnessAfter)
}

func writeLegacyCodexDoctorFixture(t *testing.T, content string, currentMatchesManifest bool) (string, *config.HarnessConfig) {
	t.Helper()
	dir := t.TempDir()
	configPath := filepath.Join(dir, ".codex", "config.toml")
	require.NoError(t, os.MkdirAll(filepath.Dir(configPath), 0o755))
	require.NoError(t, os.WriteFile(configPath, []byte(content), 0o644))

	checksum := adapter.Checksum(content)
	if !currentMatchesManifest {
		checksum = adapter.Checksum("previous generated content")
	}
	manifest := adapter.NewManifest("codex")
	manifest.Files[filepath.ToSlash(filepath.Join(".codex", "config.toml"))] = adapter.ManifestFile{
		Checksum: checksum,
		Policy:   adapter.OverwriteMerge,
	}
	require.NoError(t, manifest.Save(dir))

	cfg := config.DefaultFullConfig("legacy-project")
	cfg.Platforms = []string{"codex"}
	cfg.Quality.SupervisorModelPolicy = ""
	return dir, cfg
}
